# Solicitamos dos números enteros del usuario
num1 = int(input())
num2 = int(input())

# Calculamos la suma, la diferencia y el producto de los dos números
sum = num1 + num2
diff = num1 - num2
product = num1 * num2

# Imprimimos los resultados en tres líneas separadas
print(sum)      # Suma
print(diff)     # Diferencia
print(product)  # Producto